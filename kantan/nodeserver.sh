# Kantan NodeJS server script
# This file launches a basic NodeJS server.
# NodeJS doesn't have a built-in server usable as ac command line like Python or PHP, so the code is inside nodeserver.mjs
# You can customize it to go to the location of your app and choose which file to launch.
# Example: go to the nodeserver.mjs file and change "demo" by the folder of your choice on line 6
node kantan/nodeserver.mjs